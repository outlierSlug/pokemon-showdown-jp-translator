  /**
   * Merged translation object combining all translation maps
   * Aggregates pokemonMap, movesMap, abilitiesMap, and itemsMap
   * @type {Object<string, string>}
   * @private
   */
  const translations = {
    ...pokemonMap,
    ...movesMap,
    ...abilitiesMap,
    ...itemsMap,
  };

  /**
   * Sorted array of all translation keys
   * Keys are sorted by length (longest first) for proper regex matching priority
   * This ensures longer terms like "Ultra Ball" match before shorter "Ball"
   * @type {string[]}
   * @private
   */
  const keys = Object.keys(translations)
    .sort((a, b) => b.length - a.length); // longer first (important)

  /**
   * Compiled regular expression for fast text replacement
   * Uses word boundaries (\\b) to ensure accurate matching
   * Avoids partial word matches (e.g., "Fire" won't match in "Fireplace")
   * @type {RegExp}
   * @private
   */
  const regex = new RegExp(`\\b(${keys.join("|")})\\b`, "g");

  /**
   * Translates English game terms to Japanese using the compiled regex
   * @param {string} text - The text to translate
   * @returns {string} The translated text with English terms replaced by Japanese equivalents
   * @private
   */
  function translateText(text) {
    if (!ENABLE_TRANSLATION) return text;
    return text.replace(regex, match => translations[match] || match);
  }

  /**
   * Recursively processes DOM nodes to translate text content
   * Translates text nodes while skipping script, style, and input elements
   * Also skips the chat input area (.chatbox) to prevent translating user input
   * @param {Node} node - The DOM node to process
   * @returns {void}
   * @private
   */
  function processNode(node) {
    if (!node) return;

    // Text node: translate the text content
    if (node.nodeType === Node.TEXT_NODE) {
      node.nodeValue = translateText(node.nodeValue);
      return;
    }

    // Element node: check filters and recursively process children
    if (node.nodeType === Node.ELEMENT_NODE) {
      const tag = node.tagName;

      // Skip script, style, textarea, and input elements (don't modify these)
      if (["SCRIPT", "STYLE", "TEXTAREA", "INPUT"].includes(tag)) return;

      // Skip chat input area to preserve user input text
      if (node.closest && node.closest(".chatbox")) return;

      // Recursively process all child nodes
      for (const child of node.childNodes) {
        processNode(child);
      }
    }
  }

  /**
   * Performs initial translation of the entire page on load
   * Processes document.body and all descendants
   * @returns {void}
   * @private
   */
  function initialTranslate() {
    processNode(document.body);
    console.log("[JP Translator] Initial translation complete");
  }

  /**
   * MutationObserver instance that watches for dynamically added DOM content
   * Automatically translates new nodes as they appear in the page
   * @type {MutationObserver}
   * @private
   */
  const observer = new MutationObserver((mutations) => {
    // Process each added node from all mutations
    for (const mutation of mutations) {
      for (const node of mutation.addedNodes) {
        processNode(node);
      }
    }
  });

  /**
   * Starts the MutationObserver to watch for dynamically added content
   * Observes all child list changes throughout the document
   * @returns {void}
   * @private
   */
  function startObserver() {
    observer.observe(document.body, {
      childList: true,  // Watch for added/removed child nodes
      subtree: true,    // Watch entire document tree
    });
    console.log("[JP Translator] Observer started");
  }

  /**
   * Debug function to verify the script is running
   * Logs a message to the console for troubleshooting
   * @returns {void}
   * @private
   */
  function debugTest() {
    console.log("[JP Translator] Script running");
  }

  /**
   * Initialize the translator when the page fully loads
   * Runs debug check, performs initial translation, and starts watching for new content
   */
  window.addEventListener("load", () => {
    debugTest();
    initialTranslate();
    startObserver();
  });

